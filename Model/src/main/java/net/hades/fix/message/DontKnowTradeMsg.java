/**
 * <p>Extracted from FIX protocol documentation <a href="http://www.fixprotocol.org/">FIX Protocol</a></p>
 * The Donâ€™t Know Trade (DK) message notifies a trading partner that an electronically received execution has
 * been rejected. This message can be thought of as an execution reject message.
 * This message has special utility when dealing with one-way execution reporting. If the initial Order
 * Acknowledgment message (LastQty=0 and OrdStatus=New) does not match an existing order this message can
 * be used to notify the broker of a potential problem order.
 * Note that the decision to DK an execution lies with the institution. Some of the mismatches listed in the
 * DKReason field may be acceptable and will not require a DK messages to be generated.
 *
 * @author <a href="mailto:support@marvisan.com">Support Team</a>
 * @version $Revision: 1.4 $
 * @created 15/01/2011, 9:25:04 AM
 */
