/**
 * <p>Extracted from FIX protocol documentation <a href="http://www.fixprotocol.org/">FIX Protocol</a></p>
 * The order cancel request message requests the cancelation of all of the remaining quantity of an existing order.
 * Note that the Order Cancel/Replace Request should be used to partially cancel (reduce) an order).<br/>
 * The request will only be accepted if the order can successfully be pulled back from the exchange floor without
 * executing.<br/>
 * A cancel request is assigned a ClOrdID and is treated as a separate entity. If rejected, the ClOrdID of the
 * cancel request will be sent in the Cancel Reject message, as well as the ClOrdID of the actual order in the
 * OrigClOrdID field. The ClOrdID assigned to the cancel request must be unique amongst the ClOrdID assigned
 * to regular orders and replacement orders.<br/>
 * An immediate response to this message is required. It is recommended that an ExecutionRpt with
 * ExecType=Pending Cancel be sent unless the Order Cancel Request can be immediately accepted
 * (ExecutionRpt with ExecType=Canceled) or rejected (Order Cancel Reject message).
 *
 * @author <a href="mailto:support@marvisan.com">Support Team</a>
 * @version $Revision: 1.4 $
 * @created 16/01/2011, 9:25:04 AM
 */
